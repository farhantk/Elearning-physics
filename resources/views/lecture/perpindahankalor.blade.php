      <p align="justify">
         Saat merebus air di dalam panci, air yang berada di bagian bawah akan lebih cepat panas dibanding air yang berada di permukaan karena posisinya yang lebih dekat dengan kompor (sumber panas). Ketika air di bagian bawah sudah cukup panas, maka air akan bergerak ke atas untuk bertukar posisi dengan air di bagian atas sehingga air di dalam panci akan panas dan mendidih secara merata. 
      </p>
      <p align="justify">Secara mikroskopik, konveksi terjadi karena kalor mengalir melalui pergerakan massa molekul dari satu tempat ke tempat yang lain. Sementara konduksi melibatkan molekul yang hanya bergerak dalam jarak yang pendek dan bertabrakan. Konveksi melibatkan pergerakan dari sejumlah besar molekul pada jarak yang besar.</p>
      <p align="justify">Peristiwa perpindahan kalor melalui suatu zat disertai dengan perpindahan partikel-partikelnya ini disebut dengan konveksi. Laju kalor konveksi dapat dihitung dengan persamaan sebagai berikut.</p>
