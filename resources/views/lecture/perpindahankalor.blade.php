      <p align="justify">Dari percobaan di atas, tampak bahwa pemanasan menyebabkan zat warna potasium permanganat bergerak ke atas. Gerakan ini disebabkan oleh naiknya suhu air. Bagaimanakah peristiwa ini dapat dijelaskan? Kalor dari pembakar spiritus menaikkan suhu air. Akibatnya, volume air memuai dan massa jenis air berkurang. Karena massa jenis air berkurang, air menjadi lebih ringan sehingga air panas bergerak ke atas. Tempat yang ditinggalkan oleh air panas diisi oleh air dingin, sebab air dingin memiliki massa jenis lebih kecil daripada air panas. Jadi, air panas bergerak ke atas dan air dingin bergerak ke bawah. Hal ini tampak dari gerakan warna potasium permanganat. Pergantian antara air panas dan air dingin berlangsung terus sampai air mendidih. </p>
      <p align="justify">Tubuh manusia menghasilkan sejumlah besar energi termal yang merupakan transformasi dari energi yang didapatkan dari makanan. Energi yang didapatkan dari makanan ditransformasikan paling banyak sekitar 20% untuk digunakan melakukan usaha, sisanya 80% ditransformasikan ke dalam energi termal. Perpindahan kalor dalam tubuh dilakukan secara konveksi oleh darah.</p>


      
