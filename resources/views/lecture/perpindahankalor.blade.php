      <p align="justify">Berdasarkan percobaan di atas, dapat disimpulkan bahwa logam adalah bahan yang memiliki daya hantar kalor paling baik dibandingkan kayu dan plastik. Sebaliknya, kayu dan plastik adalah zat padat yang memiliki daya hantar buruk. Penghantar kalor yang baik dinamakan konduktor. Sebaliknya, penghantar kalor yang jelek dinamakan isolator. Bahan yang memiliki daya hantar kalor di antara konduktor dan isolator dinamakan semi konduktor.</p>
      
