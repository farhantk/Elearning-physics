      <p align="justify">
         Sebuah batang logam yang salah satu ujungnya dipanaskan dan ujung lainnya dipegang seperti gambar di atas, menunjukkan salah satu contoh proses konduksi. Proses konduksi yang terjadi yaitu ketika kita merasakan panas saat memegang batang logam tersebut, padahal bagian logam yang dipanaskan berada di ujung lainnya. Hal ini menjadi bukti bahwa ternyata logam tersebut mengalirkan kalor.
      </p>
      <p align="justify">
         Secara mikroskopik, konduksi kalor terjadi melalui tabrakan molekul. Pada saat satu sisi objek dipanaskan, molekul di bagian tersebut menjadi semakin cepat karena suhu semakin tinggi. Pada saat molekul-molekul yang lebih cepat bertabrakan dengan molekul yang lebih lambat, molekul tersebut memindahkan sebagian energi kinetiknya sehingga terjadi perubahan kecepatan gerak molekul pada molekul yang lebih lambat menjadi lebih cepat sehingga menyebabkan kenaikan suhu. Molekul ini kemudian memindahkan energinya dengan cara bertabrakan dengan molekul yang lebih jauh sepanjang objek tersebut.
      </p>
      <p align="justify">
         Peristiwa perpindahan kalor melalui suatu zat tanpa disertai dengan perpindahan partikel-partikelnya ini disebut dengan konduksi. Jumlah kalor yang dipindahkan tiap satuan waktu dapat dihitung dengan persamaan sebagai berikut.
