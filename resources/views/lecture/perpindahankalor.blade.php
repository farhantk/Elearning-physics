<p align="justify">Berdasarkan medium perantaranya, perpindahan kalor dapat dibagi menjadi 3 jenis diantaranya yaitu konduksi, konveksi, dan radiasi. Gambar di atas menjelaskan sekaligus tiga proses perpindahan kalor yang terjadi pada sebuah kasus yaitu ketika memanaskan air di dalam panci. Proses konduksi terjadi pada panas yang terdapat pada pemegang panci berbahan logam, proses konveksi terjadi pada bagian air yang dipanaskan dimana air yang sebelumnya berada di bawah bergerak untuk bertukar posisi dengan air dingin yang berada di bagian atas, dan proses radiasi terjadi pada rambatan kalor dari kompor ke panci. Untuk lebih jelasnya, proses konduksi, konveksi, dan radiasi dijelaskan sebagai berikut.</p>

